public class area {
    public static void main(String[] args) {
        double r = 5.0;
        double a = Math.PI * Math.pow(r, 2);
        double c = 2 * Math.PI * r;
        System.out.println("Radius: " + r);
        System.out.println("Area: " + a);
        System.out.println("Circumference: " + c);
    }
}
